package com.example.servingwebcontent;

public class ToDo {

    String task;
    boolean statusDone;

    public String getTask() {
        return task;
    }



    public void setTask(String task) {
        this.task = task;
    }

    public boolean isStatusDone() {
        return statusDone;
    }

    public void setStatusDone(boolean statusDone) {
        this.statusDone = statusDone;
    }
}
